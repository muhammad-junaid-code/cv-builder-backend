def sanitise_cv(cv: dict) -> dict:
    if not isinstance(cv, dict): return {}
    for f in ("totalYears","title","summary","competencies","keywords"):
        cv[f] = _to_str(cv.get(f),"")
    if cv.get("title"): cv["title"] = _normalize_job_title(cv["title"])

    companies = cv.get("companies")
    if not isinstance(companies, list): companies = []
    clean_companies = []
    for co in companies:
        if not isinstance(co, dict): continue
        raw_bullets = co.get("bullets") or []
        if not isinstance(raw_bullets, list): raw_bullets = [_to_str(raw_bullets)]
        bullets = [_to_str(b) for b in raw_bullets if b and _to_str(b)]
        tech = co.get("tech") or co.get("technologies") or co.get("stack") or ""
        if isinstance(tech, list): tech = " | ".join(_to_str(t) for t in tech if t)
        else: tech = _to_str(tech)
        clean_companies.append({
            "company":   _to_str(co.get("company") or co.get("name") or ""),
            "role":      _to_str(co.get("role")    or co.get("title") or ""),
            "dateRange": _to_str(co.get("dateRange") or co.get("dates") or co.get("date") or ""),
            "bullets":   bullets,
            "tech":      tech,
        })
    cv["companies"] = clean_companies
    if not isinstance(cv.get("skills"), list): cv["skills"] = []

    projects = cv.get("projects")
    if not isinstance(projects, list): projects = []
    _GENERIC_PROJ = re.compile(
        r'^(azure app|angular application|react (app|application|dashboard)|'
        r'web (platform|application|app)|mobile app|laravel (app|application)|'
        r'erp (platform|system)|social media platform|e-?commerce platform)$',
        re.IGNORECASE)
    clean_projects = []
    for p in projects:
        if isinstance(p, dict):
            raw_ov = _to_str(p.get("overview") or p.get("desc") or p.get("description") or "")
            raw_bl = p.get("bullets") or []
            proj_b = _to_str_list(raw_bl, [])
            ov     = raw_ov
            if not proj_b and len(raw_ov) > 80:
                sentences = re.split(r'(?<=[.!?])\s+', raw_ov.strip())
                if len(sentences) >= 2:
                    ov = " ".join(sentences[:2])
                    proj_b = [s for s in sentences[2:] if len(s)>20][:3]
            tech_tags = p.get("techTags",[])
            if not tech_tags and p.get("tech"): tech_tags = p.get("tech")
            if isinstance(tech_tags, str):
                tech_tags = [t.strip() for t in re.split(r'[|,]', tech_tags) if t.strip()]
            proj_name = _to_str(p.get("name") or p.get("title") or "")
            if ":" not in proj_name and " - " in proj_name:
                parts = proj_name.split(" - ", 1)
                if len(parts) == 2 and len(parts[0].split()) <= 5:
                    proj_name = parts[0].strip() + ": " + parts[1].strip()
            system_type = _to_str(p.get("systemType") or p.get("system_type") or p.get("type") or "")
            clean_projects.append({
                "name":       proj_name,
                "systemType": system_type,
                "overview":   ov,
                "bullets":    proj_b,
                "desc":       ov,
                "techTags":   tech_tags[:7] if tech_tags else [],
            })
        elif isinstance(p, str):
            clean_projects.append({"name":p,"overview":"","bullets":[],"desc":""})
    cv["projects"] = clean_projects[:4]

    edu_raw = cv.get("education")
    if isinstance(edu_raw, dict):
        cv["education"] = {
            "university":  _to_str(edu_raw.get("university",  STATIC_CANDIDATE["university"])),
            "degree":      _to_str(edu_raw.get("degree",      STATIC_CANDIDATE["degree"])),
            "cgpa":        _to_str(edu_raw.get("cgpa",        STATIC_CANDIDATE["gpa"])),
            "years":       _to_str(edu_raw.get("years",       "2017 - 2021")),
            "achievement": _to_str(edu_raw.get("achievement", STATIC_CANDIDATE["achievement"])),
        }
    elif not isinstance(cv.get("education"), dict):
        cv["education"] = {"university":STATIC_CANDIDATE["university"],"degree":STATIC_CANDIDATE["degree"],
                           "cgpa":STATIC_CANDIDATE["gpa"],"years":"2017 - 2021","achievement":STATIC_CANDIDATE["achievement"]}

    related = cv.get("relatedTech")
    if not isinstance(related, list): related = []
    clean_related = []
    seen_rt = set()
    for cat in related:
        if isinstance(cat, dict):
            items_raw = cat.get("items") or cat.get("technologies") or cat.get("tools") or []
            unique = []
            for it in _to_str_list(items_raw, []):
                if it.lower() not in seen_rt: seen_rt.add(it.lower()); unique.append(it)
            if unique: clean_related.append({"category":_to_str(cat.get("category") or cat.get("name") or ""),"items":unique[:13]})
    cv["relatedTech"] = clean_related[:6]

    tech_raw = cv.get("technologies")
    if isinstance(tech_raw, dict):
        seen_t = set()
        def _dedup(lst):
            result = []
            for x in (lst or []):
                xl = _to_str(x).lower()
                if xl and xl not in seen_t: seen_t.add(xl); result.append(_to_str(x))
            return result
        cv["technologies"] = {
            "mustHave":   _dedup(tech_raw.get("mustHave")   or tech_raw.get("must_have")    or []),
            "niceToHave": _dedup(tech_raw.get("niceToHave") or tech_raw.get("nice_to_have") or []),
            "additional": _dedup(tech_raw.get("additional") or tech_raw.get("other")        or []),
        }
    elif not isinstance(cv.get("technologies"), dict):
        cv["technologies"] = {"mustHave":[],"niceToHave":[],"additional":[]}

    arch_raw = cv.get("architectures")
    clean_arch = []
    if isinstance(arch_raw, list):
        for a in arch_raw:
            if isinstance(a, dict):
                name = _to_str(a.get("name") or a.get("pattern") or a.get("title") or "")
                desc = _to_str(a.get("description") or a.get("desc") or "")
                if name: clean_arch.append({"name":name,"description":desc})
            elif isinstance(a, str) and a.strip():
                clean_arch.append({"name":a.strip(),"description":""})
    cv["architectures"] = clean_arch[:5]
    return cv

