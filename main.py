@app.post("/auth/generate-key")
async def generate_key(req: GenerateKeyRequest):
    if req.admin_pass != ADMIN_PASSWORD: raise HTTPException(403, "Invalid admin password")
    keys = _load_auth_keys()
    raw  = secrets.token_hex(8).upper()
    token = f"CVAI-{raw[0:4]}-{raw[4:8]}-{raw[8:12]}"
    days  = req.expires_days or (req.days_valid if req.days_valid > 0 else 0)
    now   = datetime.utcnow()
    exp   = (now + timedelta(days=days)).isoformat() if days > 0 else None
    keys[token] = {"label":req.label,"created_at":now.isoformat(),"expires_at":exp,"active":True,"last_used":None}
    _save_auth_keys(keys)
    return {"token":token,"label":req.label,"expires_at":exp,"message":"Key generated successfully"}

@app.post("/auth/login")
async def login(req: LoginRequest):
    keys  = _load_auth_keys()
    token = req.token.strip().upper()
    entry = keys.get(token)
    if not entry: raise HTTPException(401, "Invalid key - not found")
    if not _is_token_valid(entry): raise HTTPException(401, "Key is expired or revoked")
    entry["last_used"] = datetime.utcnow().isoformat()
    keys[token] = entry; _save_auth_keys(keys)
    return {"ok":True,"token":token,"label":entry.get("label",""),"expires_at":entry.get("expires_at"),"message":"Login successful"}

@app.post("/auth/verify")
async def verify_token(req: VerifyRequest):
    keys  = _load_auth_keys()
    token = req.token.strip().upper()
    entry = keys.get(token)
    if not entry or not _is_token_valid(entry): return {"valid":False}
    return {"valid":True,"label":entry.get("label",""),"expires_at":entry.get("expires_at")}

@app.post("/auth/revoke")
async def revoke_key(req: RevokeRequest):
    if req.admin_pass != ADMIN_PASSWORD: raise HTTPException(403, "Invalid admin password")
    keys  = _load_auth_keys()
    token = req.token.strip().upper()
    if token not in keys: raise HTTPException(404, "Key not found")
    keys[token]["active"] = False; _save_auth_keys(keys)
    return {"ok":True,"message":"Key revoked"}

