async def generate_cv(req: CVRequest):
    req = req.copy(update={"job_title": _normalize_job_title(req.job_title.strip())})

    async def _run():
        provider  = req.provider.lower()
        key_used  = ""
        key_idx   = 0

        if provider == "groq":
            cv, model, key_used = await call_groq(req)
        elif provider == "cerebras":
            cv, model, key_used = await call_cerebras(req)
        elif provider == "deepseek":
            cv, model, key_used = await call_deepseek(req)
        elif provider == "openai":
            cv, model, key_used = await call_openai(req)
        elif provider == "gemini":
            cv, model, key_used = await call_gemini(req)
        elif provider == "ollama":
            cv    = await call_ollama(req)
            model = req.ollama_model or "qwen2.5:7b"
        else:
            raise HTTPException(400, f"Unknown provider: {provider}")

        # Remove internal keys before returning
        cv.pop("_techs",   None)
        cv.pop("_jd_text", None)

        return {"cv": cv, "model": model, "provider": provider,
                "key_used": mask(key_used) if key_used else ""}

    try:
        return await asyncio.wait_for(_run(), timeout=300)
    except asyncio.TimeoutError:
        raise HTTPException(504, "CV generation timed out (>5 minutes). Try again or switch provider.")
    except HTTPException: raise
    except httpx.ConnectError as e:
        if "11434" in str(e): raise HTTPException(503, "Cannot connect to Ollama. Make sure it is running.")
        raise HTTPException(503, f"Connection error: {e}")
    except Exception as e:
        raise HTTPException(500, f"Unexpected error: {e}")


