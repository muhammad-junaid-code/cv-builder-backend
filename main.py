def extract_json(raw: str) -> dict:
    raw = re.sub(r"<think>.*?</think>", "", raw, flags=re.DOTALL)
    raw = re.sub(r"```(?:json)?", "", raw).strip().strip("`").strip()
    start = raw.find("{")
    if start == -1: raise ValueError("No JSON object in response")
    end = raw.rfind("}")
    if end == -1:
        raw = _repair_json(raw[start:])
    else:
        raw = raw[start:end+1]
    raw = re.sub(r",\s*([}\]])", r"\1", raw)
    try: return json.loads(raw)
    except:
        raw = _repair_json(raw)
        raw = re.sub(r'[\x00-\x1f\x7f]', ' ', raw)
        raw = re.sub(r',\s*([}\]])', r'\1', raw)
        return json.loads(raw)

def _repair_json(j: str) -> str:
    j = j.rstrip()
    j = re.sub(r",\s*$", "", j)
    in_string = escape_next = False
    opens = []
    for ch in j:
        if escape_next:     escape_next = False; continue
        if ch == '\\' and in_string: escape_next = True; continue
        if ch == '"':       in_string = not in_string; continue
        if in_string:       continue
        if ch in ('{','['): opens.append(ch)
        elif ch in ('}',']') and opens: opens.pop()
    if in_string: j += '"'
    j = re.sub(r",\s*$", "", j.rstrip())
    for ch in reversed(opens): j += '}' if ch=='{' else ']'
    return j

def _to_str(val, fallback=""):
    if val is None: return fallback
    if isinstance(val, str): return val.strip()
    if isinstance(val, list): return ", ".join(_to_str(v) for v in val if v is not None and v != "")
    if isinstance(val, dict):
        for k in ("text","value","content","name","title"):
            if k in val: return _to_str(val[k])
        return str(val)
    return str(val).strip()

def _to_str_list(val, fallback=None):
    if fallback is None: fallback = []
    if val is None: return fallback
    if isinstance(val, str): return [val.strip()] if val.strip() else fallback
    if isinstance(val, list):
        result = []
        for item in val:
            if isinstance(item, list):   result.extend(_to_str(i) for i in item if i is not None and str(i).strip())
            elif isinstance(item, dict): result.append(_to_str(item))
            elif item is not None and str(item).strip(): result.append(str(item).strip())
        return result if result else fallback
    return [_to_str(val)] if val else fallback

