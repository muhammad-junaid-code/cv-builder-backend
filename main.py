_MONTH_NAMES = ["January","February","March","April","May","June",
                "July","August","September","October","November","December"]
_MONTH_MAP   = {m.lower(): i+1 for i, m in enumerate(_MONTH_NAMES)}
_MONTH_MAP.update({m.lower()[:3]: i+1 for i, m in enumerate(_MONTH_NAMES)})

def _month_name(n): return _MONTH_NAMES[(n-1) % 12]

def _parse_month_year(s):
    s = s.strip()
    if s.lower() == "present": return date.today()
    parts = s.split()
    if len(parts) == 2:
        return date(int(parts[1]), _MONTH_MAP.get(parts[0].lower(), 1), 1)
    raise ValueError(f"Cannot parse: {s!r}")

def _months_between(a, b): return max(0,(b.year-a.year)*12+(b.month-a.month))

def _add_months(d, m):
    total = d.month - 1 + m
    return date(d.year + total//12, total%12+1, 1)

def _subtract_months(d, m): return _add_months(d, -m)

def _calc_total_years(years_exp=""):
    if years_exp:
        try:
            n = float(years_exp.strip().replace("+",""))
            return str(int(n)) if n == int(n) else str(round(n,1))
        except ValueError: pass
    total = 0
    for co in CANDIDATE_COMPANIES:
        try:
            total += _months_between(_parse_month_year(co["start"]), _parse_month_year(co["end"]))
        except: pass
    y = total/12
    return "5+" if y>=5 else "4+" if y>=4 else "3+" if y>=3 else "2+" if y>=2 else "1+"

def _build_dynamic_companies(years_exp="", num_companies=0):
    if not years_exp:
        n_co = num_companies if num_companies>0 else 3
        return CANDIDATE_COMPANIES[:n_co]
    try: n = float(years_exp.strip().replace("+",""))
    except: return CANDIDATE_COMPANIES[:max(1,num_companies)]
    total = int(round(n*12))
    today = date.today()
    def fmt(d): return f"{_month_name(d.month)} {d.year}"
    num_cos = num_companies if num_companies>0 else (1 if n<=1.4 else 2 if n<=2.4 else 3)
    each = total//num_cos if num_cos else total
    remainder = total - each*num_cos
    result, cursor = [], today
    for i in range(num_cos):
        span    = each + (remainder if i==0 else 0)
        start   = _subtract_months(cursor, span)
        end_str = "Present" if i==0 else fmt(cursor)
        name    = CANDIDATE_COMPANIES[i]["name"] if i<len(CANDIDATE_COMPANIES) else f"Company {i+1}"
        result.append({"name": name, "start": fmt(start), "end": end_str})
        cursor  = start
    return result

def _build_education_year(years_exp="", profile_edu=None):
    today = date.today()
    if profile_edu:
        e = profile_edu[0]
        p_from = str(e.get("from","") or "").strip()
        p_to   = str(e.get("to","")   or "").strip()
        if p_from and p_to:   return {"start": p_from, "end": p_to}
        if p_to and not p_from:
            try: return {"start": str(int(p_to[:4])-4), "end": p_to}
            except: pass
        if p_from and not p_to:
            try: return {"start": p_from, "end": str(int(p_from[:4])+4)}
            except: pass
    if years_exp:
        try:
            n   = int(float(years_exp.strip().replace("+","")))
            end = today.year - max(n-1, 0)
            return {"start": str(end-4), "end": str(end)}
        except: pass
    return {"start": "2017", "end": "2021"}

