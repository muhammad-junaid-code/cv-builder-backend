# POST-PROCESSING — strips generic words, deduplicates, no tech knowledge
# ─────────────────────────────────────────────────────────────────────────────
def _apply_buzzword_fixes(text: str) -> str:
    _FIXES = [
        (r'\bHighly motivated\b','Experienced'),(r'\bResults-driven\b','Delivery-focused'),
        (r'\bDynamic professional\b','Experienced professional'),(r'\bPassionate about\b','Focused on'),
        (r'\bLeveraged\b','Used'),(r'\bUtilized\b','Used'),(r'\bUtilised\b','Used'),
        (r'\bRevolutionary\b','Effective'),(r'\bCutting-edge\b','Modern'),
        (r'\bNext-generation\b','Modern'),(r'\bAI-powered\b','Automated'),
        (r'\bInnovative solution\b','technical solution'),(r'\bSynergy\b','collaboration'),
        (r'\bSeamlessly\b','effectively'),(r'\bRobust solution\b','reliable system'),
        (r'\bTransformed\b','Improved'),(r'\b10x\b',''),
    ]
    for pattern, repl in _FIXES:
        text = re.sub(pattern, repl, text, flags=re.IGNORECASE)
    return text

def _mandatory_enforce(valid_skills: list, core: list, preferred: list) -> list:
    """Only enforce items that look like real named tools — no generic words."""
    def _looks_real(t: str) -> bool:
        t = t.strip()
        if len(t) < 2: return False
        if t.isupper() and len(t) >= 2: return True
        if any(c in t for c in ".-#+/"): return True
        words = t.split()
        _generic = {"good","strong","solid","ability","understanding","knowledge","skills",
                    "experience","proficiency","tools","techniques","team","teams","work",
                    "working","communication","analysis","tracking","analytics","content",
                    "writing","reporting","management","planning","strategy","research","monitoring"}
        if len(words) >= 2:
            return not all(w.lower() in _generic for w in words)
        w = words[0]
        if w[0].isupper() and len(w) > 2:
            if w.lower().endswith(("ing","tion","sion","ment","ness","ity","ism")): return False
            return True
        return False

    mandatory = [t for t in list(dict.fromkeys(core + preferred)) if _looks_real(t)]
    flat_lower = " ".join(valid_skills).lower()
    missing    = [t for t in mandatory if len(t) > 2 and t.lower() not in flat_lower]

    if missing:
        print(f"[SKILLS-ENFORCE] Injecting {len(missing)} missing: {missing}")
        best_idx, best_len = 0, 0
        for i, s in enumerate(valid_skills):
            c = s.find(":")
            if c > 0:
                n = len([x.strip() for x in s[c+1:].split(",") if x.strip()])
                if n > best_len: best_len = n; best_idx = i
        colon = valid_skills[best_idx].find(":")
        cat   = valid_skills[best_idx][:colon].strip()
        items = [x.strip() for x in valid_skills[best_idx][colon+1:].split(",") if x.strip()]
        space = max(0, 13 - len(items))
        items.extend(missing[:space])
        overflow = missing[space:]
        valid_skills[best_idx] = f"{cat}: {', '.join(items)}"
        if overflow:
            for i2, s2 in enumerate(valid_skills):
                if i2 == best_idx: continue
                c2 = s2.find(":")
                if c2 > 0:
                    cat2  = s2[:c2].strip()
                    itm2  = [x.strip() for x in s2[c2+1:].split(",") if x.strip()]
                    sp2   = max(0, 13 - len(itm2))
                    itm2.extend(overflow[:sp2])
                    overflow = overflow[sp2:]
                    valid_skills[i2] = f"{cat2}: {', '.join(itm2)}"
                if not overflow: break
    return valid_skills

def post_process_cv(cv: dict, validated: dict, years_exp: str = "") -> dict:
    """Clean up CV: fix buzzwords, sanitize skills, normalize tech strings."""
    # Buzzword fixes in summary and bullets
    if cv.get("summary"):
        cv["summary"] = _apply_buzzword_fixes(cv["summary"])
    for co in cv.get("companies", []):
        co["tech"] = _sanitize_tech_string(co.get("tech",""))
        co["bullets"] = [_apply_buzzword_fixes(b) for b in (co.get("bullets") or [])]

    # Fix skills
    skills = cv.get("skills", [])
    if isinstance(skills, list):
        skills = [_clean_skill_row(s) for s in skills if s]
        skills = _sanitize_skills_list(skills)
        core      = validated.get("validated_core",      [])
        preferred = validated.get("validated_preferred", [])
        skills = _mandatory_enforce(skills, core, preferred)
        cv["skills"] = skills

    # Normalize totalYears
    ty = _calc_total_years(years_exp)
    cv["totalYears"] = ty

    # Fix title duplicates
    if cv.get("title"):
        cv["title"] = _normalize_job_title(cv["title"])

    return cv

