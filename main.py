# Bad endings for single-word items that make them look like gerunds/nouns
_BAD_ENDINGS = ("ing","tion","sion","ment","ness","ity","ism","age")

def _is_valid_skill_item(item: str) -> bool:
    """
    Return True if this item is a real named tool/platform/methodology/technique.
    Completely generic — no technology names hardcoded.
    """
    item = item.strip()
    if len(item) < 2: return False

    words = item.split()

    # Multi-word items: keep unless ALL words are in the generic set
    if len(words) >= 2:
        if all(w.lower() in _GENERIC_WORDS for w in words): return False
        # Also reject clearly truncated items
        if len(item) <= 3: return False
        return True

    # Single word
    w = words[0]
    wl = w.lower()

    # All-caps acronyms are always valid (GSC, GA4, GEO, SEO, SQL, AWS, API...)
    if w.isupper() and len(w) >= 2: return True

    # Blocklist check
    if wl in _GENERIC_WORDS: return False

    # Contains special chars → likely a real product name (C#, ASP.NET, Node.js, E-E-A-T)
    if any(c in w for c in ".-#+/:"): return True

    # Contains digits → version number or acronym (GA4, Python3, .NET8)
    if any(c.isdigit() for c in w): return True

    # PascalCase or mixed-case proper noun
    if w[0].isupper() and len(w) > 2:
        if wl.endswith(_BAD_ENDINGS): return False
        return True

    # All-lowercase single word not in blocklist — likely still generic
    return False

def _sanitize_skills_list(skills: list) -> list:
    """
    Light sanitiser that preserves AI output structure.
    Only removes items that are provably generic English words.
    Never renames categories. Never adds hardcoded technologies.
    """
    cleaned = []
    for row in skills:
        if not row or not isinstance(row, str): continue
        if ":" not in row: cleaned.append(row); continue
        colon = row.index(":")
        cat   = row[:colon].strip()
        items = [t.strip() for t in re.split(r"[,|;·•]", row[colon+1:].strip()) if t.strip()]
        real  = [t for t in items if _is_valid_skill_item(t)]
        if len(real) >= 3:
            cleaned.append(f"{cat}: {', '.join(real)}")
    return cleaned

def _sanitize_tech_string(tech: str) -> str:
    if not tech: return ""
    sep   = "|" if "|" in tech else ","
    parts = [t.strip() for t in tech.split(sep) if t.strip()]
    real  = [t for t in parts if _is_valid_skill_item(t)]
    return " | ".join(real) if real else ""

def _clean_skill_row(row: str) -> str:
    colon = row.find(":")
    if colon <= 0: return row
    cat   = row[:colon].strip()
    items = [p.strip() for p in re.split(r"[|,]", row[colon+1:].strip()) if p.strip()]
    seen, unique = set(), []
    for item in items:
        if item.lower() not in seen:
            seen.add(item.lower()); unique.append(item)
    return f"{cat}: {', '.join(unique)}"

